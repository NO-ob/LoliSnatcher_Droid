  /// Get a random kaomoji from a specific category.
  ///
  /// Example:
  /// ```dart
  /// final happyFace = KaomojiData.random(KaomojiCategory.joy);
  /// ```
  static String random(KaomojiCategory category) {
    final list = kaomojis[category]!;
    return list[Random().nextInt(list.length)];
  }

  /// Get a random kaomoji from any category.
  ///
  /// Example:
  /// ```dart
  /// final randomFace = KaomojiData.randomAny();
  /// ```
  static String randomAny() {
    const categories = KaomojiCategory.values;
    final category = categories[Random().nextInt(categories.length)];
    return random(category);
  }

  /// Get a specific kaomoji by category and index.
  ///
  /// The index wraps around if it exceeds the category size.
  ///
  /// Example:
  /// ```dart
  /// final shrug = KaomojiData.get(KaomojiCategory.indifference, 11); // Â¯\_(ãƒ„)_/Â¯
  /// ```
  static String get(KaomojiCategory category, int index) {
    final list = kaomojis[category]!;
    return list[index % list.length];
  }

  /// Get all kaomojis in a category.
